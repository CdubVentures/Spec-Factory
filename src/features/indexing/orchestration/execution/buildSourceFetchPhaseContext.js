export function buildSourceFetchPhaseContext({
  fetcher,
  fetcherMode,
  config,
  logger,
  fetchHostConcurrencyGate,
  fetchWithModeFn,
  runWithRetryFn,
  classifyFetchOutcomeFn,
  bumpHostOutcomeFn,
  applyHostBudgetBackoffFn,
  resolveHostBudgetStateFn,
  toIntFn,
  resumeFetchFailedUrls,
  frontierDb,
  productId,
  maybeApplyBlockedDomainCooldownFn,
  repairQueryContext,
  maybeEmitRepairQueryFn,
  blockedDomainHitCount,
  blockedDomainThreshold,
  blockedDomainsApplied,
  planner,
  normalizeHostTokenFn,
  hostFromHttpUrlFn,
  traceWriter,
} = {}) {
  return {
    fetcher,
    fetcherMode,
    config,
    logger,
    fetchHostConcurrencyGate,
    fetchWithModeFn,
    runWithRetryFn,
    classifyFetchOutcomeFn,
    bumpHostOutcomeFn,
    applyHostBudgetBackoffFn,
    resolveHostBudgetStateFn,
    toIntFn,
    resumeFetchFailedUrls,
    frontierDb,
    productId,
    repairQueryContext,
    maybeEmitRepairQueryFn,
    maybeApplyBlockedDomainCooldownFn: (payload) => maybeApplyBlockedDomainCooldownFn({
      ...payload,
      blockedDomainHitCount,
      blockedDomainThreshold,
      blockedDomainsApplied,
      planner,
      logger,
      normalizeHostTokenFn,
      hostFromHttpUrlFn,
    }),
    traceWriter,
  };
}
